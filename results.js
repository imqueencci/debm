/**
 * Pattern Code 배열을 분석하여 결과 반환
 * @param {string[]} codes - 선택된 Pattern Code 배열
 * @returns {Object} 결과 객체
 */
function calculateResult(codes) {
  const counts = {};
  codes.forEach(c => { counts[c] = (counts[c] || 0) + 1; });
  const top = Object.entries(counts).sort((a, b) => b[1] - a[1])[0][0];
  return RESULTS[PATTERN_TO_RESULT[top] || 'floater'];
}
